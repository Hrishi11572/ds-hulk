'''
# Characteristics of Dictionaries

1. Key-Value Pairs:
    - Key-Value Pairs: A dictionary consists of unique keys and their corresponding values.

    - Keys act as identifiers for values and must be immutable (e.g., strings, numbers, or tuples).

Values can be of any data type and can even be duplicates.

2. Unordered: Dictionaries are unordered collections, meaning their items are not stored in a specific sequence.

3. Mutable: Dictionaries are mutable, meaning you can add, update, or delete key-value pairs after creation.

4. Dynamic: Dictionaries can grow or shrink in size as needed.

'''

student_data = {
    "alice" : 21, 
    "bobby" : 23, 
    "jacobi" : 25, 
    "alexandra" : 30
}

