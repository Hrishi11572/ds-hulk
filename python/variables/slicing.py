# the MOST used syntax


'''
    string_name[ start : end : step ]
    
    where the `start` index is inclusive, while the `end` index is enclusive 
'''


text = "Python Programming"

print(text[:1])
print(text[:9:2])
print(text[4:10:1])
print(text[-1::-1]) # print in reverse 
print(text[-1::-2]) # print in reverse skipping a letter 

